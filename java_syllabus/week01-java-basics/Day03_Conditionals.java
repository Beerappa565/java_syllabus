// Day 3: Conditionals
// Theory: Java supports if-else, else-if, and switch-case for decision making.

public class Day03_Conditionals {
    public static void main(String[] args) {
        int num = 7;
        if (num % 2 == 0) {
            System.out.println("Even");
        } else {
            System.out.println("Odd");
        }
    }
}
