// Day 2: Operators
// Theory: Java supports arithmetic, relational, logical, and bitwise operators.

public class Day02_Operators {
    public static void main(String[] args) {
        int a = 10, b = 5;
        System.out.println("Sum: " + (a + b));
        System.out.println("Is a > b? " + (a > b));
    }
}
